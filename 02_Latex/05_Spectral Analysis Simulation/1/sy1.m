clear all
k = -20:0.1:20;
a1 = -0.95;
b1 = -0.9;
sigma = 1;
d1 = (-a1).^(k-1).*(k -1>=0 & k -1<= 19) + (-a1).^(-k+1).*(k-1>=-21 & k-1 < 0);
d2 = (-a1).^(k).*(k>=0 & k<= 20) + (-a1).^(-k).*(k>=-20 & k < 0);
d3 = (-a1).^(k+1).*(k +1>=0 & k +1<= 21) + (-a1).^(-k-1).*(k+1>=-19 & k+1 < 0);

r = sigma./(1-a1.^2).*[b1.*d1 + (1+b1.^2).*d2 + b1.*d3]
plot(k, r)
xlabel('k')
ylabel('r(k)')
title(['a1=',num2str(a1),', b1=', num2str(b1)])

genacs(20, 1, -0.9, -0.8);